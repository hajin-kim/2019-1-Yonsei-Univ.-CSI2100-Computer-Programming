def zeroCheck(num1, num2, num3):
    """
    Check zero
    :param num1: int
    :param num2: int
    :param num3: int
    :return: If it is zero, return Boolean True.
    """
    # Use this inspector statement
    # print(zeroCheck(int(input()),int(input()),int(input())))
    return num1 * num2 * num3 == 0
